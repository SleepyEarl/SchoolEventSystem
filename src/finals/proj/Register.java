    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String email = jTextField1.getText().trim();
        String studentId = jTextField2.getText().trim();
        String department = Dept.getSelectedItem().toString();
        String password = new String(jPasswordField1.getPassword()).trim();
             
        if(email.isEmpty() || studentId.isEmpty() || password.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this,"Please fill in the fields");
            return;
        }
        
        boolean success = UserAuth.registerUser(email, studentId, department, password);
        if(success) {
            javax.swing.JOptionPane.showMessageDialog(this, "Registration Complete!");
            Login loginFrame = new Login();
            loginFrame.setVisible(true);
            loginFrame.pack();
            loginFrame.setLocationRelativeTo(null);
            this.dispose();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Email already Registered!");
        }
        
             try (FileWriter fw = new FileWriter("userData.txt", true);
             BufferedWriter bw = new BufferedWriter(fw);
             PrintWriter out = new PrintWriter(bw)) {
            
             out.println(email + "," + studentId + "," + department + "," + password);

            javax.swing.JOptionPane.showMessageDialog(this, "Registration Successful!");
          } catch (IOException e) {
     }
   
    }//GEN-LAST:event_jButton1ActionPerformed

