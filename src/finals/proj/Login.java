    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String email = jTextField1.getText().trim();
        String password = new String(jPasswordField1.getPassword()).trim();
        
        if(email.isEmpty() || password.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please fill in all the field");
            return;
        }
        
        if(UserAuth.loginUser(email, password)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Login Suggessful!");
            Dashboard dash = new Dashboard();
            dash.setVisible(true);
            dash.pack();
            dash.setLocationRelativeTo(null);
            this.dispose();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Invalid Credentials! Please Register first!");
            Register reg = new Register();
            reg.setVisible(true);
            reg.pack();
            reg.setLocationRelativeTo(null);
            this.dispose();
        }
        
     
  
    }//GEN-LAST:event_jButton1ActionPerformed

